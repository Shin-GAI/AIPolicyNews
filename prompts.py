# ── 카테고리 정의 ───────────────────────────────────────────
# key: telegram_sender.py 의 CATEGORY_EMOJI 키와 일치해야 합니다.
CATEGORIES: dict[str, str] = {
    "policy": "AI 정책·규제",
    "tech":   "AI 기술·연구",
    "infra":  "AI 인프라·산업",
    "safety": "AI 안전·윤리",
}


# ── 시스템 프롬프트 ─────────────────────────────────────────
def build_system_prompt() -> str:
    return (
        "당신은 AI 정책·기술 전문 뉴스 에디터입니다.\n"
        "매일 아침 독자들이 핵심만 빠르게 파악할 수 있도록 "
        "간결하고 신뢰할 수 있는 브리핑을 작성합니다.\n\n"
        "작성 원칙:\n"
        "1. 오늘 날짜 기준 최신 동향을 중심으로 작성합니다.\n"
        "2. 불필요한 수식어 없이 사실 중심으로 기술합니다.\n"
        "3. 주요 이슈 3~5개를 **굵게** 표시된 제목과 함께 요약합니다.\n"
        "4. 각 이슈는 2~3문장 이내로 간결하게 정리합니다.\n"
        "5. 한국어로 작성합니다."
    )


# ── 사용자 프롬프트 ─────────────────────────────────────────
def build_user_prompt(category_name: str, today: str) -> str:
    return (
        f"오늘은 {today}입니다.\n\n"
        f"[{category_name}] 분야의 오늘자 주요 뉴스·동향을 브리핑해 주세요.\n\n"
        "형식:\n"
        "**이슈 제목**\n"
        "핵심 내용 2~3문장.\n\n"
        "(3~5개 이슈)"
    )
